    <h2>A propos</h2>
    <br />
    Shotgun est une micro-billetterie pour les associations de l'Icam.<br />
    Le but est de proposer à la vente des places pour divers événements, les événements qui utilisent cette billetterie nécessitent
    qu'un utilisateur n'achète qu'une et une seule place.<br />
    Le projet est open-source et accepte très volontiers les suggestions sous formes "d'issues" ou de "pull-request" sur <a href="https://github.com/mattgu74/shotgunutc" >github. </a>

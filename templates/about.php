    <h2>A propos</h2>
    <br />
    ShotgunUTC est une micro-billetterie. <br />
    Le but est de proposer à la vente des places pour divers événements, les événements qui utilise cette billetterie implique
    qu'un utilisateur n'achète qu'une et une seule place.<br />
    De plus dans la majorité des cas, l'utilisateur en plus de devoir être utcéen, doit être cotisant BDE-UTC.<br/><br/>
    Le projet est open-source et accepte très volontiers les suggestions sous formes "d'issues" ou de "pull-request" sur <a href="https://github.com/mattgu74/shotgunutc" >github. </a>
